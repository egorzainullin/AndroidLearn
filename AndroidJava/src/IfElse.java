public class IfElse {
    public static void main(String[] args) {
        int a = 5;
        if (a > 10) {
            System.out.println("pizza");
        } else if (a >= 6 && a <= 10) {
            System.out.println("hamburger");
        } else {
            System.out.println("sandwich");
        }
    }
}
